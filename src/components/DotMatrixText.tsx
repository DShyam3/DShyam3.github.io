const DotMatrixChar = ({ char }: { char: string }) => {
    const upperChar = char.toUpperCase();
    const pattern = charPatterns[upperChar] || charPatterns[' '];

    return (
        <div className="dot-matrix-char">
            {pattern.map((row, rowIndex) => (
                <div key={rowIndex} className="dot-matrix-row">
                    {row.map((dot, colIndex) => (
                        <div
                            key={colIndex}
                            className={`dot-matrix-dot ${dot ? 'active' : ''}`}
                        />
                    ))}
                </div>
            ))}
        </div>
    );
};

interface DotMatrixTextProps {
    text: string;
    size?: 'xs' | 'sm' | 'md' | 'lg';
    className?: string;
}

export const DotMatrixText = ({ text, size = 'md', className = '' }: DotMatrixTextProps) => {
    const sizeClass =
        size === 'lg' ? 'dot-matrix-text-lg' :
            size === 'sm' ? 'dot-matrix-text-sm' :
                size === 'xs' ? 'dot-matrix-text-xs' :
                    '';
    return (
        <div className={`dot-matrix-text ${sizeClass} ${className}`}>
            {text.split('').map((char, index) => (
                <DotMatrixChar key={index} char={char} />
            ))}
        </div>
    );
};
